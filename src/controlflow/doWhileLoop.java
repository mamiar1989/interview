package controlflow;


public class doWhileLoop {


    public static void main(String[] args) {
        // first it will execute one time then check the condition
        int num=26;
        do{// do block; execute this block
            System.out.println("We are learning do while loop");
            num--;
        }while (num>20);


//************************************************************


        int n=55;
        do{
            System.out.println("This is  a do while loop practice ");n--;
        }while(n>5);

//*********************************************************************

        int bn=22;
        do {
            System.out.println("This is a beautiful day ");bn++;
        }while(bn>=25);


    }



}



